const DICTS: Record<Locale, Dict> = { en, ja, "zh-hans": zhHans };

/** exposed for tests: assert translation key parity across locales */
export const LOCALE_DICTS: Record<Locale, Dict> = DICTS;

export type TFunc = (key: string, ...args: (string | number)[]) => string;

export function makeT(locale: Locale): TFunc {
  const dict = DICTS[locale] ?? en;
  return (key, ...args) => {
    let s = dict[key] ?? en[key] ?? key;
    args.forEach((a, i) => {
      s = s.replace(`{${i}}`, String(a));
    });
    return s;
  };
}
