/**
 * Lightweight i18n for the diagram viewer and extension UI (en / ja / zh-Hans,
 * matching the localized READMEs). Pure — shared by the webview (React) and the
 * extension host. Locale is resolved from `vscode.env.language` and passed to
 * the webview in the model message.
 *
 * `t(key, ...args)` looks up the active locale, falls back to English, then to
 * the key itself; `{0}`, `{1}` … are replaced by positional args.
 */
export type Locale = "en" | "ja" | "zh-hans";

/** map a VS Code language tag (e.g. "ja", "zh-cn", "pt-br") to a supported locale */
export function resolveLocale(lang: string | undefined): Locale {
  const l = (lang ?? "en").toLowerCase();
  if (l.startsWith("ja")) return "ja";
  if (l.startsWith("zh")) return "zh-hans"; // we ship Simplified; Hant falls back here
  return "en";
}

